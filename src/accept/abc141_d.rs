use std::collections::binary_heap::BinaryHeap;

fn main() {
    input! {
        n: usize,
        m: usize,
        a: [u64;n]
    }
    let mut h = BinaryHeap::<u64>::new();
    h.extend(a.iter());
    let mut c = 0;
    while c < m {
        let mut x = h.peek_mut().unwrap();
        *x /= 2;
        c+=1;
    }
    println!("{}", h.iter().sum::<u64>());
}

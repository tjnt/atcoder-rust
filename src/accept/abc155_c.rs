use std::collections::HashMap;

fn main() {
    input! {
        n: usize,
        ss: [String;n]
    }
    let mut h = HashMap::new();
    for s in ss {
        *h.entry(s).or_insert(0) += 1;
    }
    let m = h.values().max().unwrap();
    let mut v = h.iter().filter(|&(_,v)| {
        v == m
    }).map(|t| t.0).collect::<Vec<_>>();
    v.sort();
    for k in v {
        println!("{}", k);
    }
}
